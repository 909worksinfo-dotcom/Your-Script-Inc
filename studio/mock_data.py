def _mock_storyboard(start, end, mode):
    out = []
    if mode == "comic":
        header = "镜号,场景,画面内容 (Visual),Commentary & Key Dialogue"
        n_shots = 26
    else:
        header = "镜号,场景,画面内容 (Visual),台词 (Dialogue) & 音效 (SFX)"
        n_shots = 22
    scenes = ["贫民窟出租屋", "上流宴会厅", "地铁隧道", "黑市档口", "数据中枢", "中央服务器"]
    for ep in range(start, end + 1):
        out.append(f"第 {ep} 集")
        out.append(header)
        for shot in range(1, n_shots + 1):
            scene = scenes[(ep + shot) % len(scenes)]
            visual = f"凯在{scene}里情绪爆发 动作充满张力 画面强冲击力"
            if mode == "comic":
                if shot % 6 == 0:
                    dialogue = ""  # 纯画面镜头 Silent Shot
                elif shot % 3 == 0:
                    dialogue = f'Kai: I can finally see you, episode {ep}'
                else:
                    dialogue = f"Narrator: I bought my eyes back in episode {ep} shot {shot}"
            else:
                if shot % 4 == 0:
                    dialogue = f"SFX: Glitch sound and dramatic sting"
                else:
                    dialogue = f"Kai: I will tear off your mask in episode {ep}"
            out.append(f"{shot},{scene},{visual},{dialogue}")
    return "\n".join(out)
