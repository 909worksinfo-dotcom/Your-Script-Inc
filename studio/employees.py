# ==========================================
# 系统提示拼装 = 角色介绍 + 工作技能
# 默认严格采用 PRD 原文；同时支持用户在产品里自定义覆盖（见 persona_from_store）。
# ==========================================
def default_skills_block(emp_key):
    """某数字员工「工作技能」的默认文本块（每条一行，带 - 前缀），用于展示与拼装系统提示。"""
    return "\n".join([f"- {s}" for s in EMPLOYEES[emp_key]["skills"]])


def build_persona_text(name, intro, skills_block):
    """由（可被用户编辑的）角色介绍 + 工作技能文本块拼装数字员工系统提示。"""
    return (
        f"你是「{name}」。\n\n"
        f"【角色介绍】\n{intro}\n\n"
        f"【工作技能】\n{skills_block}\n\n"
        "请在本次任务中根据上述工作技能，发挥你的全部能力。"
    )


def _persona(emp_key):
    e = EMPLOYEES[emp_key]
    return build_persona_text(e["name"], e["intro"], default_skills_block(emp_key))


def persona_from_store(store, emp_key):
    """执行任务时使用的系统提示：优先用用户在 store 里自定义的设定（角色介绍 / 工作技能），
    留空或未自定义则回退到 PRD 默认设定。"""
    e = EMPLOYEES[emp_key]
    overrides = (getattr(store, "emp_settings", None) or {}).get(emp_key) or {}
    intro = (overrides.get("intro") or "").strip() or e["intro"]
    skills_block = (overrides.get("skills") or "").strip() or default_skills_block(emp_key)
    return build_persona_text(e["name"], intro, skills_block)


