def get_task_method(tid, overrides=None):
    """取任务 tid 的「协作方式 / 工作方法」：用户自定义非空则用自定义，否则用 PRD 默认。"""
    if overrides:
        v = overrides.get(tid)
        if isinstance(v, str) and v.strip():
            return v
    return TASK_METHODS[tid]


def extract_outline_word_count_requirement(method):
    """从用户可编辑的任务协作方式里提取每集分集大纲字数要求。

    支持常见写法，例如：
    - 每一集分集大纲字数必须为300-500字
    - 每集正文三段合计 800-1000 字
    - 每集不少于600字 / 每集至少600字
    如果用户没有写明确数字，回退为产品默认 300-500 字。
    """
    text = (method or "").strip()
    range_match = re.search(r"(?:每一集|每集)[^\n。；;]*?(\d{2,5})\s*[-~—－到至]\s*(\d{2,5})\s*字", text)
    if range_match:
        low, high = sorted(int(x) for x in range_match.groups())
        return (
            f"每一集正文（【开头高光】+【正片】+【结尾悬念】三段合计）的字数必须落在 {low}-{high} 字之间，"
            f"必须卡在这个范围内：低于 {low} 字一律判定为不合格、需要补足；高于 {high} 字需精简。"
        )

    min_match = re.search(r"(?:每一集|每集)[^\n。；;]*?(?:不少于|至少|不低于|最低)\s*(\d{2,5})\s*字", text)
    if min_match:
        low = int(min_match.group(1))
        return (
            f"每一集正文（【开头高光】+【正片】+【结尾悬念】三段合计）的字数必须不少于 {low} 字，"
            f"低于 {low} 字一律判定为不合格、需要补足。"
        )

    return (
        "每一集正文（【开头高光】+【正片】+【结尾悬念】三段合计）的字数必须落在 300-500 字之间，"
        "必须卡在这个范围内：低于 300 字一律判定为不合格、需要补足；高于 500 字需精简。"
    )


def build_instruction(tid, method, total_episodes=50):
    """执行时动态拼装任务 1/3/4/6/7 的完整指令：标题(HEAD) + 工作方法(method) + 输出要求(FOOT)。
    注意：只对 FOOT 做 .format（FOOT 由我们掌控），不对用户输入的 method 做 format，避免用户文本里的
    花括号触发 KeyError/格式化异常（零 bug）。"""
    foot = TASK_INSTR_FOOT[tid]
    if "{outline_word_count_requirement}" in foot:
        foot = foot.replace(
            "{outline_word_count_requirement}",
            extract_outline_word_count_requirement(method),
        )
    if "{total_episodes}" in foot:
        foot = foot.format(total_episodes=total_episodes)
    return TASK_INSTR_HEAD[tid] + (method or "") + foot
